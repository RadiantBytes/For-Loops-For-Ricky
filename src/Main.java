/*
 * For Loops For Ricky
 * 
 *  A quick program to help my good pal Ricky
 *  with his for loops
 *  
 *  @author Thomas Lynaugh
 *  
 *  Last Modified: February 20, 2019
 */
public class Main {

	public static void main(String[] args) {

		for (int i = 1; i < 7; i++) {
			for (int d = i; d < 7; d++) {

				System.out.print(d * 10 + " ");
			}
			System.out.println();

		}

	}

}
